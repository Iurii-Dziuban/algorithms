package iurii.job.interview.algorithms2.coursera;

public class Point2D {
    public final double x;
    public final double y;

    public Point2D(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public String toString() {
        return "[" + x + " , " + y + "]";
    }
}
